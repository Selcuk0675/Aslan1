package day03typecastingwrapperscanner;

public class WrapperClass01 {
    public static void main(String[] args) {
        /*
        Java primitivelere method ekleyerek Wrapper Clası olusturur
         Prımıtıve     Wrapper
         byte   ==>   Byte
         int    ==>   Integer
         char   ==>   Character

         normalde prımıtıvelerın yanına nokta koydugumuzda metod gelmez
         fakat wpapper yaptıgımızda nokta koydugumuzda bır suru metod gelır


         */

        System.out.println(Byte.MAX_VALUE);
        System.out.println(Integer.MIN_VALUE);
        System.out.println(Character.MAX_VALUE);
    }

}
